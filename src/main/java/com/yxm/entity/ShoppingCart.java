package com.yxm.entity;

public class ShoppingCart {
    public String cartID;
    public String userName;
    public String shoppingID;
    public String shoppingName;
    public String shoppingImg;
    public String shoppingPrice;

    public String getCartID() {
        return cartID;
    }

    public void setCartID(String cartID) {
        this.cartID = cartID;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getShoppingID() {
        return shoppingID;
    }

    public void setShoppingID(String shoppingID) {
        this.shoppingID = shoppingID;
    }

    public String getShoppingName() {
        return shoppingName;
    }

    public void setShoppingName(String shoppingName) {
        this.shoppingName = shoppingName;
    }

    public String getShoppingImg() {
        return shoppingImg;
    }

    public void setShoppingImg(String shoppingImg) {
        this.shoppingImg = shoppingImg;
    }

    public String getShoppingPrice() {
        return shoppingPrice;
    }

    public void setShoppingPrice(String shoppingPrice) {
        this.shoppingPrice = shoppingPrice;
    }
}
