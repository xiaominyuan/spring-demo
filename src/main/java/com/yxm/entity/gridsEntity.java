package com.yxm.entity;

public class gridsEntity {
    public String gridID;
    public String gridName;
    public String gridPrice;
    public String gridKucun;

    public String getGridID() {
        return gridID;
    }

    public void setGridID(String gridID) {
        this.gridID = gridID;
    }

    public String getGridName() {
        return gridName;
    }

    public void setGridName(String gridName) {
        this.gridName = gridName;
    }

    public String getGridPrice() {
        return gridPrice;
    }

    public void setGridPrice(String gridPrice) {
        this.gridPrice = gridPrice;
    }

    public String getGridKucun() {
        return gridKucun;
    }

    public void setGridKucun(String gridKucun) {
        this.gridKucun = gridKucun;
    }
}
